    public void loadSubcategories() {
    try {
        Class.forName("oracle.jdbc.driver.OracleDriver");
        Connection con = DriverManager.getConnection(
            "jdbc:oracle:thin:@localhost:1521/XEPDB1", "asset", "asset");

        PreparedStatement pst = con.prepareStatement(
            "SELECT * FROM asset_subcategories ORDER BY slno");
        ResultSet rs = pst.executeQuery();

        DefaultTableModel model = new DefaultTableModel(
            new Object[]{"S.No", "Category", "Subcategory"}, 0
        ) {
            public boolean isCellEditable(int row, int col) {
                return false;
            }
        };

        while (rs.next()) {
            model.addRow(new Object[]{
                rs.getInt("slno"),
                rs.getString("cat_name"),
                rs.getString("subcat_name")
            });
        }

        tblSubcategories.setModel(model);
        con.close();
    } catch (Exception e) {
        JOptionPane.showMessageDialog(this, "Error loading subcategories: " + e.getMessage());
    }
}

    public void loadCategories() {
    try {
        Class.forName("oracle.jdbc.driver.OracleDriver");
        Connection con = DriverManager.getConnection(
            "jdbc:oracle:thin:@localhost:1521/XEPDB1", "asset", "asset");

        PreparedStatement pst = con.prepareStatement(
            "SELECT cat_name FROM asset_categories WHERE is_active = 'Active'");
        ResultSet rs = pst.executeQuery();

        cmbCategory.removeAllItems();
        while (rs.next()) {
            cmbCategory.addItem(rs.getString("cat_name"));
        }

        con.close();
    } catch (Exception e) {
        JOptionPane.showMessageDialog(this, "Error loading categories: " + e.getMessage());
    }
}

