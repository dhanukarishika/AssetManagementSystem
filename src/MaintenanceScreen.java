    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        // TODO add your handling code here:
        try {
        String asset = cmbAsset.getSelectedItem().toString();
        String issue = txtIssue.getText();
        java.util.Date date = dateMaintenance.getDate();
        java.sql.Date sqlDate = new java.sql.Date(date.getTime());
        double cost = Double.parseDouble(jTextField2.getText()); // rename later to txtCost
        String status = cmbStatus.getSelectedItem().toString();

        Class.forName("oracle.jdbc.driver.OracleDriver");
        Connection con = DriverManager.getConnection(
            "jdbc:oracle:thin:@localhost:1521/XEPDB1", "asset", "asset");

        PreparedStatement pst = con.prepareStatement(
            "INSERT INTO asset_maintenance VALUES (maintenance_seq.NEXTVAL, ?, ?, ?, ?, ?)");
        pst.setString(1, asset);
        pst.setString(2, issue);
        pst.setDate(3, sqlDate);
        pst.setDouble(4, cost);
        pst.setString(5, status);

        pst.executeUpdate();
        JOptionPane.showMessageDialog(this, "Maintenance added âœ…");
        loadMaintenanceTable(); // refresh table
        con.close();
    } catch (Exception e) {
        JOptionPane.showMessageDialog(this, "Error: " + e.getMessage());
    }

    }//GEN-LAST:event_btnAddActionPerformed

