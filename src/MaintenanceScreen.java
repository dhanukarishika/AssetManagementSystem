    public void loadMaintenanceTable() {
    try {
        Class.forName("oracle.jdbc.driver.OracleDriver");
        Connection con = DriverManager.getConnection(
            "jdbc:oracle:thin:@localhost:1521/XEPDB1", "asset", "asset");

        PreparedStatement pst = con.prepareStatement(
            "SELECT * FROM asset_maintenance ORDER BY maintenance_id DESC");
        ResultSet rs = pst.executeQuery();

        DefaultTableModel model = new DefaultTableModel(
            new Object[]{"ID", "Asset", "Issue", "Date", "Cost", "Status"}, 0
        );

        while (rs.next()) {
            model.addRow(new Object[]{
                rs.getInt("maintenance_id"),
                rs.getString("asset_name"),
                rs.getString("issue_reported"),
                rs.getDate("maintenance_date"),
                rs.getDouble("cost"),
                rs.getString("status")
            });
        }

        tblMaintenance.setModel(model);
        con.close();
    } catch (Exception e) {
        JOptionPane.showMessageDialog(this, "Error loading data: " + e.getMessage());
    }
}

    public void loadAssets() {
    try {
        Class.forName("oracle.jdbc.driver.OracleDriver");
        Connection con = DriverManager.getConnection(
            "jdbc:oracle:thin:@localhost:1521/XEPDB1", "asset", "asset");

        PreparedStatement pst = con.prepareStatement(
    "SELECT asset_name FROM asset_register WHERE status IN ('Working', 'Broken', 'Available')");

        ResultSet rs = pst.executeQuery();

        cmbAsset.removeAllItems();
        while (rs.next()) {
            cmbAsset.addItem(rs.getString("asset_name"));
        }

        con.close();
    } catch (Exception e) {
        JOptionPane.showMessageDialog(this, "Error loading assets: " + e.getMessage());
    }
}


