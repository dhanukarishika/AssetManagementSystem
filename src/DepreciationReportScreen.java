    private void btnExportPDFActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExportPDFActionPerformed
        // TODO add your handling code here:
        try {
    JFileChooser chooser = new JFileChooser();
    chooser.setSelectedFile(new File("depreciation_report.pdf"));
    int option = chooser.showSaveDialog(this);

    if (option == JFileChooser.APPROVE_OPTION) {
        File file = chooser.getSelectedFile();

        Document document = new Document();
        PdfWriter.getInstance(document, new FileOutputStream(file));
        document.open();

        PdfPTable table = new PdfPTable(tblReport.getColumnCount());

        // Add table headers
        for (int i = 0; i < tblReport.getColumnCount(); i++) {
            table.addCell(new PdfPCell(new Phrase(tblReport.getColumnName(i))));
        }

        // Add table rows
        for (int i = 0; i < tblReport.getRowCount(); i++) {
            for (int j = 0; j < tblReport.getColumnCount(); j++) {
                Object val = tblReport.getValueAt(i, j);
                table.addCell(val != null ? val.toString() : "");
            }
        }

        document.add(table);
        document.close();

        JOptionPane.showMessageDialog(this, "PDF Exported Successfully ✅");
    }
} catch (Exception e) {
    JOptionPane.showMessageDialog(this, "Error exporting PDF: " + e.getMessage());
}

    }//GEN-LAST:event_btnExportPDFActionPerformed

