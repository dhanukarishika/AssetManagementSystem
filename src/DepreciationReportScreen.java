public void loadReport() {
    try {
        Class.forName("oracle.jdbc.driver.OracleDriver");
        Connection con = DriverManager.getConnection(
            "jdbc:oracle:thin:@localhost:1521/XEPDB1", "asset", "asset");

        String category = cmbFilterCat.getSelectedItem().toString();
        String status = cmbFilterStatus.getSelectedItem().toString();
        String year = txtYear.getText().trim();

        String query = "SELECT * FROM asset_register WHERE 1=1";
        if (!category.equals("All")) {
            query += " AND category = ?";
        }
        if (!status.equals("All")) {
            query += " AND status = ?";
        }
        if (!year.isEmpty()) {
            query += " AND EXTRACT(YEAR FROM purchase_date) = ?";
        }

        PreparedStatement pst = con.prepareStatement(query);
        int index = 1;
        if (!category.equals("All")) pst.setString(index++, category);
        if (!status.equals("All")) pst.setString(index++, status);
        if (!year.isEmpty()) pst.setString(index++, year);

        ResultSet rs = pst.executeQuery();
        DefaultTableModel model = (DefaultTableModel) tblReport.getModel();
        model.setRowCount(0);

        while (rs.next()) {
            Object[] row = {
                rs.getInt("serial_no"),
                rs.getString("asset_name"),
                rs.getString("category"),
                rs.getDate("purchase_date"),
                rs.getDouble("purchase_price"),
                rs.getInt("num_units"),
                rs.getDouble("original_value"),
                rs.getDouble("depreciation"),
                rs.getDouble("residual_value"),
                rs.getString("status")
            };
            model.addRow(row);
        }

        con.close();
    } catch (Exception e) {
        JOptionPane.showMessageDialog(this, "Error loading report: " + e.getMessage());
    }
}


