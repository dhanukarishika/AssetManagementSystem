    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        // TODO add your handling code here:
        try {
    Class.forName("oracle.jdbc.driver.OracleDriver");
    Connection con = DriverManager.getConnection(
        "jdbc:oracle:thin:@localhost:1521/XEPDB1", "asset", "asset");

    String cat = txtCategory.getText();
    String active = cmbActive.getSelectedItem().toString();

    PreparedStatement pst = con.prepareStatement(
        "INSERT INTO asset_categories VALUES (asset_cat_seq.NEXTVAL, ?, ?)");
    pst.setString(1, cat);
    pst.setString(2, active);

    pst.executeUpdate();
    JOptionPane.showMessageDialog(this, "Category Added ✅");
    loadCategories(); // reloads the table with the new entry


    con.close();
} catch (Exception e) {
    JOptionPane.showMessageDialog(this, "Error: " + e.getMessage());
}

    }//GEN-LAST:event_btnAddActionPerformed

