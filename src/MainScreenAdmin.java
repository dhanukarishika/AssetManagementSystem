    public void loadStats() {
    try {
        Class.forName("oracle.jdbc.driver.OracleDriver");
        Connection con = DriverManager.getConnection(
            "jdbc:oracle:thin:@localhost:1521/XEPDB1", "asset", "asset");

        Statement st = con.createStatement();

        ResultSet rs1 = st.executeQuery("SELECT COUNT(*) FROM asset_register");
        if (rs1.next()) lblTotal.setText(rs1.getInt(1) + "");

        ResultSet rs2 = st.executeQuery("SELECT COUNT(*) FROM asset_register WHERE status = 'Available'");
        if (rs2.next()) lblAvailable.setText(rs2.getInt(1) + "");

        ResultSet rs3 = st.executeQuery("SELECT COUNT(*) FROM asset_register WHERE status = 'Assigned'");
        if (rs3.next()) lblAssigned.setText(rs3.getInt(1) + "");

        ResultSet rs4 = st.executeQuery("SELECT COUNT(*) FROM asset_register WHERE status = 'Retired'");
        if (rs4.next()) lblRetired.setText(rs4.getInt(1) + "");

        con.close();
    } catch (Exception e) {
        JOptionPane.showMessageDialog(this, "Error loading stats: " + e.getMessage());
    }
}

